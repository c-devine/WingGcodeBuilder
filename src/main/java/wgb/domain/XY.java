package wgb.domain;

public class XY {

	private Double X;
	private Double Y;

	public XY()
	{

	}

	public XY(Double x, Double y) {
		this.X = x;
		this.Y = y;
	}

	public Double getX() {
		return X;
	}

	public void setX(Double x) {
		X = x;
	}

	public Double getY() {
		return Y;
	}

	public void setY(Double y) {
		Y = y;
	}

}
